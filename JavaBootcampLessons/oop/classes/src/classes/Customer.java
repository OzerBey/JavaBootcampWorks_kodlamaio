package classes;

public class Customer {
	int id;
	String firstName;
	String lastName;
	double balance;

	public Customer() {
		
	}

	public Customer(int id, String firstName, String lastName, double balance) {
		super();
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.balance = balance;
	}

}
